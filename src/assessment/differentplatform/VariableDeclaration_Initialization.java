package assessment.differentplatform;

public class VariableDeclaration_Initialization {
    public static void main(String[] args) {
      // Sample o/p-  Integer: 25 Double: 3.14 Boolean: true Character: A
        byte by = 120;
        short sh = 32000;
        int a = 2000000000;
        long l = 1234567891234506789L;
        float f = 3331.4251789411f;
        double d = 3331.4251789411;
        boolean b = true;
        char c = 'A';

        System.out.println("Byte: " + by);
        System.out.println("Short: " + sh);
        System.out.println("Integer: " + a);
        System.out.println("Long: " + l);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Boolean: " + b);
        System.out.println("Character: " + c);
    }
}
