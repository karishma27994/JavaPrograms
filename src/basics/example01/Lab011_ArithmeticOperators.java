package basics.example01;

public class Lab011_ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10, b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

       /*Arithmetic Operators (Used for basic math)
        + -> Addition
        - -> Subtraction
        * -> multiplication
        / -> Division (returns quotient as output)
        % -> Division (returns remainder as output)*/
    }
}
