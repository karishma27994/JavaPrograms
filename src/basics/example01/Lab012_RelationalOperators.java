package basics.example01;

public class Lab012_RelationalOperators {
    public static void main(String[] args) {
        int x =5, y =6;
        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x!=y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x==y);

 /*       Relational Operators-Used with true/false values (Compares 2 values and returns output as true or false)
        < -> Less than
        > -> greater than
        <= -> less than equal to
        >= -> greater than equal to
        == -> equal to
        != -> not equal to */
    }
}
