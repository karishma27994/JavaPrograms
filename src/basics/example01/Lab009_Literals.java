package basics.example01;

public class Lab009_Literals {
    public static void main(String[] args) {
        //Literals are just a value assigned to the variable(Identifier)

        //Integral literal
        byte b = 10;          // int literal, fits in byte
        short s = 20;         // int literal, fits in short
        int i = 1000;         // int literal (default)
        long l = 123456789L;  // long literal (with L)

        //floating point literal
        float f = 3.14F;
        double d = 3.14477;

        //Character Literals
        char letter = 'A';
        char digit = '9';
        char symbol = '#';
        char unicode = '\u0915';

        //Boolean literals
        boolean b1 = true;
        boolean b2 = false;

        //String Literals
        String name = "Java";

        //Null Literals
        String blank = null;
    }
}
